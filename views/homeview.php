    <h2>Nos valeurs</h2>

    <p>
        Située sur l’Île de Ré, la crêperie <strong>CrêpeRiz</strong> est née d’une passion
        pour la cuisine authentique et le respect des produits.  
        Chaque jour, nous mettons tout en œuvre pour offrir à nos clients une expérience
        gourmande, simple et sincère, dans un cadre convivial et chaleureux.
    </p>

    <p>
        Notre établissement s’inscrit dans une démarche responsable, en accord avec
        les valeurs de notre territoire. Nous croyons qu’une bonne cuisine commence
        par de bons ingrédients et par le respect de ceux qui les produisent.
    </p>

    <h3>Une cuisine locale et de qualité</h3>
    <p>
        Nous privilégions autant que possible les <strong>circuits courts</strong> et les
        <strong>producteurs locaux</strong> de l’Île de Ré et de la région.
        Farines, œufs, produits laitiers ou cidre sont sélectionnés avec soin afin
        de garantir fraîcheur, goût et traçabilité.
    </p>

    <h3>Un engagement éco-responsable</h3>
    <p>
        Conscients de la richesse naturelle qui nous entoure, nous adoptons une
        <strong>démarche éco-responsable</strong> au quotidien :
        limitation du gaspillage alimentaire, tri des déchets, utilisation
        d’emballages recyclables et choix de fournisseurs engagés.
    </p>

    <h3>Le fait maison et le savoir-faire</h3>
    <p>
        Toutes nos galettes et crêpes sont <strong>préparées sur place</strong>,
        selon des recettes traditionnelles, avec une attention particulière portée
        aux cuissons et aux associations de saveurs.
        Le fait maison est pour nous une garantie de qualité et d’authenticité.
    </p>

    <h3>Convivialité et accueil</h3>
    <p>
        CrêpeRiz, c’est aussi un lieu de <strong>partage et de convivialité</strong>.
        Que vous soyez de passage sur l’Île de Ré ou habitué du quartier, nous vous
        accueillons avec le sourire, en salle ou sur notre terrasse, pour un moment
        gourmand et détendu.
    </p>
